    public function rules()
    {
        return [
            [['username','password'],'required'],
            [['username'], 'string', 'max' => 50],
            [['password'], 'string', 'max' => 100],
            [['authKey', 'accessToken'], 'string', 'max' => 150],
        ];
    }

    /**
     * {@inheritdoc}
